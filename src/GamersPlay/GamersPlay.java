    private void jS_volumenStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jS_volumenStateChanged
        vol = jS_volumen.getValue();
        player.getMediaPlayer().setVolume(vol);
        
        if(vol == 0)
        {
            jL_vol.setIcon(new javax.swing.ImageIcon(getClass().getResource("/lib/images/icon-mute-white-32x32.png")));
        }
        else if(vol >= 1 && vol <= 30)
        {
            jL_vol.setIcon(new javax.swing.ImageIcon(getClass().getResource("/lib/images/icon-volumenBajo-white-32x32.png")));
        }
        else if(vol >= 31 && vol <= 60)
        {
            jL_vol.setIcon(new javax.swing.ImageIcon(getClass().getResource("/lib/images/icon-volumenMedio-white-32x32.png")));
        }
        else if(vol >= 61)
        {
            jL_vol.setIcon(new javax.swing.ImageIcon(getClass().getResource("/lib/images/icon-volumenAlto-white-32x32.png")));
        }
    }//GEN-LAST:event_jS_volumenStateChanged

    private void jS_progresoStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jS_progresoStateChanged
        if(!AccionProgres)
        {
            float posicion = jS_progreso.getValue()/(float)tamJs; 
            player.getMediaPlayer().setPosition(posicion);
        }
        float posicion = jS_progreso.getValue()/(float)tamJs;
        int transcurridoSeg = Math.round((posicion * duracion) / 1000)-1;
        jL_transcurrido.setText(funciones.convertirSeg(transcurridoSeg));
    }//GEN-LAST:event_jS_progresoStateChanged
    
