    private void jL_playMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jL_playMouseClicked
        if(!reproduciendo)
        {   
            jL_play.setIcon(new javax.swing.ImageIcon(getClass().getResource("/lib/images/icon-pause-white-32x32.png")));
            player.getMediaPlayer().play();
            reproduciendo = true;
        }
        else
        {
            jL_play.setIcon(new javax.swing.ImageIcon(getClass().getResource("/lib/images/icon-play-white-32x32.png")));
            player.getMediaPlayer().setPause( player.getMediaPlayer().isPlaying()?true:false );
            reproduciendo = false;
        }
    }//GEN-LAST:event_jL_playMouseClicked

